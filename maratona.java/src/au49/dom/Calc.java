package au49.dom;

public class Calc {
    public int somaCalc(int num1, int num2){
        return num1 + num2;
    }

    public int subCalc(int num1, int num2){
        return num1 - num2;
    }

    public int multCalc(int num1, int num2){
        return num1 * num2;
    }

    public double divCalc(double num1, double num2){
        return num1 / num2;
    }
}
